    public void displayReviewFeedback(Customer customer, Order order) {
        this.customer = customer;
        this.order = order;
        task = null;
        
        lblVendorName.setText(order.getVendor().getName());
        lblDate.setText(order.getOrderDate());
        lblService.setText(order.getServiceType());
        
        if (order.getServiceType().equals("Delivery")) {
            DeliveryTask deliveryTask = new DeliveryTask();
            deliveryTask.setOrder(order);
            task = (DeliveryTask) deliveryTask.getDeliveryTask(true);
            try {
                if (task.getDeliveryID() != null) {
                    deliveryReviewPanel.setVisible(true);
                }
            } catch (NullPointerException e) {
                deliveryReviewPanel.setVisible(false);
            }
        } else {
            deliveryReviewPanel.setVisible(false);
        }
    }

