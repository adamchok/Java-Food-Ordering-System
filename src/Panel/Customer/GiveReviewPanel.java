    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        Time time = new Time();
        String reviewDate = time.getDate();
        
        String vendorComments = txtVendorComments.getText();
        int vendorRating = sliderVendorRating.getValue();
        VendorReview vendorReview = new VendorReview(order.getOrderID(), vendorComments, reviewDate, vendorRating,
        order.getVendor());
        
        DeliveryReview deliveryReview = null;
        
        if (deliveryReviewPanel.isVisible()) {
            String deliveryComments = txtDeliveryComments.getText();
            int deliveryRating = sliderDeliveryRating.getValue();
            deliveryReview = new DeliveryReview(task.getDeliveryID(), deliveryComments, reviewDate, deliveryRating, 
            task.getDeliveryRunner());
        }
        
        if (deliveryReview != null) {
            if (vendorReview.writeReview(true) && deliveryReview.writeReview(false)) {
                JOptionPane.showMessageDialog(this, "Review submission successful.");
            } else {
                JOptionPane.showMessageDialog(this, "Review submission unsuccessful.");
            }
        } else {
            if (vendorReview.writeReview(true)) {
                JOptionPane.showMessageDialog(this, "Review submission successful.");
            } else {
                JOptionPane.showMessageDialog(this, "Review submission unsuccessful.");
            }
        }
        HistoryPanel historyPanel = (HistoryPanel) this.getParent();
        historyPanel.displayOrderHistory(customer);
    }//GEN-LAST:event_btnSubmitActionPerformed

