    public void displayProfile(Customer customer) {
        this.customer = customer;
        clearErrorLabels();
        txtPassword.setEchoChar('*');
        
        txtName.setText(customer.getName());
        txtPhoneNumber.setText(customer.getPhoneNumber());
        txtEmailAddress.setText(customer.getEmailAddress());
        lblGenderType.setText(customer.getGender());
        txtPassword.setText(customer.getUserLogin().getPassword());
        
        txtName.setEnabled(false);
        txtPhoneNumber.setEnabled(false);
        txtEmailAddress.setEnabled(false);
        txtPassword.setEnabled(false);
    }
    
    
    public void displayTopUp() {
        lblCreditAmount.setText("RM "+String.format("%.2f", customer.getCustomerWallet().getCurrentCreditAmount()));
        
        txtTopUpAmount.setText("");
        lblDate.setText("");
        lblTime.setText("");
        lblAmount.setText("");
        
        topUp = (TopUp) new TopUp(customer).getObject("Pending Top-up");
        
        if (topUp != null) {
            lblStatus.setText(topUp.getTopUpStatus());
            lblDate.setText(topUp.getTopUpDate());
            lblTime.setText(topUp.getTopUpTime());
            lblAmount.setText("RM "+String.format("%.2f", topUp.getTopUpAmount()));
            txtTopUpAmount.setEnabled(false);
            btnRequest.setEnabled(false);
        } else {
            lblStatus.setText("No Requests");
            txtTopUpAmount.setEnabled(true);
            btnRequest.setEnabled(true);
        }
    }
    
    public void clearErrorLabels() {
        lblNameError.setText("");
        lblEmailError.setText("");
        lblPhoneError.setText("");
        lblPasswordError.setText("");
    }

