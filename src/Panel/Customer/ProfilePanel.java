    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        clearErrorLabels();
        
        String name = txtName.getText();
        String emailAddress = txtEmailAddress.getText();
        String phoneNumber = txtPhoneNumber.getText();
        
        String password = txtPassword.getText();
        
        int error = 0;

        if (name.isEmpty()) {
            lblNameError.setText("Invalid name");
            error++;
        }
        if (emailAddress.isEmpty() || !emailAddress.contains("@") || !emailAddress.contains(".com")) {
            lblEmailError.setText("Invalid email address");
            error++;
        }
        if (phoneNumber.isEmpty() || phoneNumber.length() != 10) {
            lblPhoneError.setText("Invalid phone number");
            error++;
        }
        if (password.isEmpty()) {
            lblPasswordError.setText("Invalid password");
            error++;
        }
        
        if (error == 0) {
            customer.setName(name);
            customer.setPhoneNumber(phoneNumber);
            customer.setEmailAddress(emailAddress);
            customer.updatePassword(password);
            
            customer.getUserLogin().updateData("Login");
            if (customer.updateData("Customer") == true) {
                JOptionPane.showMessageDialog(this, "Update successful.");
                btnSave.setVisible(false);
                displayProfile(customer);
                
                MainPanel parent = (MainPanel) getParent();
                parent.updateCustomer(customer);
                
            } else {
                JOptionPane.showMessageDialog(this, "Update unsuccessful. Please try again.");
            }
        }
        displayProfile(customer);
        btnSave.setVisible(false);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        String amount = txtTopUpAmount.getText();
        
        try {
            double topUpAmount = Double.parseDouble(amount);
            topUp = new TopUp(customer, topUpAmount);
            if (topUp.requestTopUp()) {
                JOptionPane.showMessageDialog(this, "Top-up request successful.");
                txtTopUpAmount.setText("");
                btnRefresh.doClick();
            } else {
                JOptionPane.showMessageDialog(this, "Top-up request unsuccessful. Please try again.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a numeric number. Please try again.");
        }
    }//GEN-LAST:event_btnRequestActionPerformed

